    -- Trigger de validación: solo administradores pueden aprobar mentores
    CREATE OR REPLACE TRIGGER trg_val_mentor_aprobador
    BEFORE INSERT OR UPDATE ON mentores
    FOR EACH ROW
    DECLARE
        v_es_admin NUMBER;
    BEGIN
        IF :NEW.aprobado_por IS NOT NULL THEN
            SELECT COUNT(*)
            INTO v_es_admin
            FROM cuenta_roles cr
            JOIN roles r ON r.id = cr.rol_id
            WHERE cr.cuenta_id = :NEW.aprobado_por
            AND r.nombre = 'administrador'
            AND cr.estado = 1;

            IF v_es_admin = 0 THEN
                RAISE_APPLICATION_ERROR(-20003,
                    'Solo un administrador puede aprobar mentores.');
            END IF;
        END IF;
    END;
