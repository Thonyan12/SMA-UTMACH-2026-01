    CREATE TABLE cuentas (
        id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        uuid                RAW(16)         DEFAULT SYS_GUID() NOT NULL UNIQUE,
        correo              VARCHAR2(180)   NOT NULL UNIQUE,
        password_hash       VARCHAR2(500)   NOT NULL,
        ultimo_acceso       TIMESTAMP,
        estado              NUMBER(1)       DEFAULT 1 NOT NULL,
        fecha_creacion      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        fecha_actualizacion TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        CONSTRAINT ck_cuentas_pwd_len CHECK (LENGTH(password_hash) > 20),
        CONSTRAINT ck_cuentas_correo_utmach CHECK (REGEXP_LIKE(correo, '^[A-Za-z0-9._%+-]+@utmachala\.edu\.ec$')),
        CONSTRAINT ck_cuentas_estado CHECK (estado IN (0,1))
    );

    CREATE INDEX idx_cuentas_estado ON cuentas (estado);
    CREATE INDEX idx_cuentas_acc ON cuentas (ultimo_acceso);

    CREATE TABLE perfiles (
