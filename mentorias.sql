    CREATE OR REPLACE VIEW vw_mentores_estadisticas AS
    SELECT
        m.id AS mentor_id,
        NVL(ROUND(AVG(c.puntaje_total), 2), 0) AS promedio_calificacion,
        COUNT(DISTINCT CASE WHEN s.estado_sesion = 'completada' THEN s.id END) AS total_sesiones
    FROM mentores m
    LEFT JOIN solicitudes_mentoria sm ON sm.mentor_id = m.id
    LEFT JOIN sesiones_mentoria s ON s.solicitud_id = sm.id
    LEFT JOIN calificaciones c ON c.sesion_id = s.id AND c.estado = 1
    WHERE m.estado = 1
    GROUP BY m.id;

    CREATE OR REPLACE VIEW vw_mentores_top AS
    SELECT
        cta.id AS cuenta_id,
        p.nombres || ' ' || p.apellidos AS nombre_completo,
        cta.correo,
        car.nombre AS carrera,
        est.promedio_calificacion,
        est.total_sesiones,
        m.estado_aprobacion
    FROM mentores m
    JOIN perfiles_academicos pa ON pa.id = m.academico_id
    JOIN perfiles p ON p.id = pa.perfil_id
    JOIN cuentas cta ON cta.id = p.cuenta_id
    JOIN carreras car ON car.id = pa.carrera_id
    JOIN vw_mentores_estadisticas est ON est.mentor_id = m.id
    WHERE m.estado = 1 AND m.estado_aprobacion = 'aprobado';

    CREATE OR REPLACE VIEW vw_sesiones_realizadas AS
    SELECT
        ses.id,
        p_est.nombres || ' ' || p_est.apellidos AS estudiante,
        p_men.nombres || ' ' || p_men.apellidos AS mentor,
        ma.nombre AS materia,
        ses.inicio AS fecha_hora_inicio,
        ses.fin AS fecha_hora_fin,
        ses.estado_sesion,
        cal.puntaje_total AS calificacion
    FROM sesiones_mentoria ses
    JOIN solicitudes_mentoria sol ON sol.id = ses.solicitud_id
    JOIN estudiantes e ON e.id = sol.estudiante_id
    JOIN perfiles_academicos pa_est ON pa_est.id = e.academico_id
    JOIN perfiles p_est ON p_est.id = pa_est.perfil_id
    JOIN mentores m ON m.id = sol.mentor_id
    JOIN perfiles_academicos pa_men ON pa_men.id = m.academico_id
    JOIN perfiles p_men ON p_men.id = pa_men.perfil_id
    JOIN materias ma ON ma.id = sol.materia_id
    LEFT JOIN calificaciones cal ON cal.sesion_id = ses.id AND cal.estado = 1;

    CREATE OR REPLACE VIEW vw_usuarios_activos AS
    WITH stats AS (
        SELECT cta.id AS cuenta_id,
            (SELECT COUNT(*) FROM solicitudes_mentoria sm
                JOIN estudiantes e ON e.id = sm.estudiante_id
                JOIN perfiles_academicos pa ON pa.id = e.academico_id
                JOIN perfiles p2 ON p2.id = pa.perfil_id
                WHERE p2.cuenta_id = cta.id) AS solicitudes_realizadas,
            (SELECT COUNT(*) FROM sesiones_mentoria ses
                JOIN solicitudes_mentoria sm ON sm.id = ses.solicitud_id
                JOIN estudiantes e ON e.id = sm.estudiante_id
                JOIN perfiles_academicos pa ON pa.id = e.academico_id
                JOIN perfiles p2 ON p2.id = pa.perfil_id
                WHERE p2.cuenta_id = cta.id) AS sesiones_participadas
        FROM cuentas cta
    )
    SELECT cta.id AS cuenta_id,
        p.nombres || ' ' || p.apellidos AS nombre_completo,
        cta.correo,
        s.solicitudes_realizadas,
        s.sesiones_participadas,
        cta.ultimo_acceso
    FROM cuentas cta
    JOIN perfiles p ON p.cuenta_id = cta.id
    JOIN stats s ON s.cuenta_id = cta.id
    WHERE cta.estado = 1;

    -- CORRECCIÓN I: LISTAGG es 100% compatible con Oracle 11g+ y 19c
    CREATE OR REPLACE VIEW vw_materias_solicitadas AS
    WITH solicitudes_stats AS (
        SELECT materia_id,
            COUNT(id) AS total_solicitudes,
            COUNT(CASE WHEN estado_solicitud = 'aceptada' THEN 1 END) AS aceptadas
        FROM solicitudes_mentoria
        GROUP BY materia_id
    ),
    carreras_materia AS (
        SELECT cm.materia_id,
            LISTAGG(c.nombre, ', ') WITHIN GROUP (ORDER BY c.nombre) AS carreras
        FROM carrera_materias cm
        JOIN carreras c ON c.id = cm.carrera_id
        GROUP BY cm.materia_id
    )
    SELECT
        ma.id,
        ma.codigo,
        ma.nombre,
        cm.carreras,
        NVL(ss.total_solicitudes, 0) AS total_solicitudes,
        NVL(ss.aceptadas, 0) AS aceptadas
    FROM materias ma
    LEFT JOIN carreras_materia cm ON cm.materia_id = ma.id
    LEFT JOIN solicitudes_stats ss ON ss.materia_id = ma.id
    WHERE ma.estado = 1;

