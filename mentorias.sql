    CREATE OR REPLACE PACKAGE BODY pkg_mentorias AS
        PROCEDURE sp_crear_solicitud (
            p_estudiante_id IN NUMBER,
            p_materia_id IN NUMBER,
            p_descripcion IN VARCHAR2,
            p_fecha_hora IN TIMESTAMP,
            p_prioridad IN VARCHAR2
        ) IS
            v_exists NUMBER;
        BEGIN
            -- Validar: la materia pertenece a la carrera del estudiante
            BEGIN
                SELECT 1 INTO v_exists
                FROM estudiantes e
                JOIN perfiles_academicos pa ON pa.id = e.academico_id
                JOIN carreras c ON c.id = pa.carrera_id
                JOIN carrera_materias cm ON cm.carrera_id = c.id
                WHERE e.id = p_estudiante_id
                AND cm.materia_id = p_materia_id
                AND ROWNUM = 1;
            EXCEPTION WHEN NO_DATA_FOUND THEN v_exists := 0; END;
            
            IF v_exists = 0 THEN
                RAISE_APPLICATION_ERROR(-20001, 'La materia solicitada no pertenece a la carrera del estudiante.');
            END IF;

            INSERT INTO solicitudes_mentoria (estudiante_id, materia_id, descripcion, fecha_hora_deseada, prioridad, estado_solicitud)
            VALUES (p_estudiante_id, p_materia_id, p_descripcion, p_fecha_hora, p_prioridad, 'pendiente');
        END sp_crear_solicitud;

        PROCEDURE sp_programar_sesion (
            p_solicitud_id IN NUMBER,
            p_inicio IN TIMESTAMP,
            p_fin IN TIMESTAMP,
            p_enlace IN VARCHAR2
        ) IS
            v_mentor_id NUMBER;
            v_overlap_count NUMBER;
        BEGIN
            SELECT mentor_id INTO v_mentor_id FROM solicitudes_mentoria WHERE id = p_solicitud_id;
            IF v_mentor_id IS NULL THEN
                RAISE_APPLICATION_ERROR(-20006, 'La solicitud no tiene mentor asignado.');
            END IF;

            SELECT COUNT(*) INTO v_overlap_count
            FROM sesiones_mentoria ses
            JOIN solicitudes_mentoria sol ON sol.id = ses.solicitud_id
            WHERE sol.mentor_id = v_mentor_id
            AND ses.estado_sesion NOT IN ('cancelada')
            AND (
                (p_inicio >= ses.inicio AND p_inicio < ses.fin) OR
                (p_fin > ses.inicio AND p_fin <= ses.fin) OR
                (p_inicio <= ses.inicio AND p_fin >= ses.fin)
            );

            IF v_overlap_count > 0 THEN
                RAISE_APPLICATION_ERROR(-20005, 'El mentor ya tiene una sesión programada que se solapa.');
            END IF;

            INSERT INTO sesiones_mentoria (solicitud_id, inicio, fin, enlace_teams, estado_sesion)
            VALUES (p_solicitud_id, p_inicio, p_fin, p_enlace, 'programada');
        END sp_programar_sesion;

        PROCEDURE sp_agregar_disponibilidad (
            p_mentor_id IN NUMBER,
            p_dia_id IN NUMBER,
            p_inicio_min IN NUMBER,
            p_fin_min IN NUMBER
        ) IS
            v_overlap_count NUMBER;
        BEGIN
            SELECT COUNT(*) INTO v_overlap_count
            FROM disponibilidad_mentor
            WHERE mentor_id = p_mentor_id
            AND dia_id = p_dia_id
            AND activo = 1
            AND (
                (p_inicio_min >= hora_inicio_min AND p_inicio_min < hora_fin_min) OR
                (p_fin_min > hora_inicio_min AND p_fin_min <= hora_fin_min) OR
                (p_inicio_min <= hora_inicio_min AND p_fin_min >= hora_fin_min)
            );
            
            IF v_overlap_count > 0 THEN
                RAISE_APPLICATION_ERROR(-20011, 'El horario de disponibilidad se solapa con uno existente.');
            END IF;
            
            INSERT INTO disponibilidad_mentor (mentor_id, dia_id, hora_inicio_min, hora_fin_min)
            VALUES (p_mentor_id, p_dia_id, p_inicio_min, p_fin_min);
        END sp_agregar_disponibilidad;
    END pkg_mentorias;
