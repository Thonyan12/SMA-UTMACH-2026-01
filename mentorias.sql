    -- ====================================================================
    -- SECCIÓN 0: LIMPIEZA DEL ESQUEMA (Idempotencia)
    -- ====================================================================
    BEGIN
        FOR t IN (SELECT table_name FROM user_tables WHERE table_name IN (
            'HISTORIAL_CAMBIOS', 'TABLAS_SISTEMA', 'NOTIFICACIONES', 'CALIFICACIONES', 'SESIONES_MENTORIA', 'SOLICITUDES_MENTORIA',
            'DISPONIBILIDAD_MENTOR', 'CAT_DIAS', 'MENTOR_ESPECIALIDADES', 'CARRERA_MATERIAS', 'MATERIAS', 'CUENTA_ROLES',
            'MENTORES', 'ESTUDIANTES', 'ROLES', 'CAT_ESTADO_SESION', 'CAT_ESTADO_SOLICITUD', 'CAT_ESTADO_APROBACION',
            'FACULTAD_DIRECTORES', 'PERFILES_ACADEMICOS', 'CARRERAS', 'FACULTADES', 'PERFILES', 'CUENTAS'
        )) LOOP
            EXECUTE IMMEDIATE 'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS';
        END LOOP;
    END;
    /

