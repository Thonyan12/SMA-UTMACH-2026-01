    CREATE TABLE historial_cambios (
        id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        tabla_id            NUMBER          NOT NULL,
        registro_id         NUMBER          NOT NULL,
        accion              VARCHAR2(20)    NOT NULL,
        datos_anteriores    CLOB,
        datos_nuevos        CLOB,
        cuenta_id           NUMBER,
        ip_origen           VARCHAR2(45),
        descripcion         VARCHAR2(4000),
        db_user             VARCHAR2(128)   DEFAULT SYS_CONTEXT('USERENV', 'SESSION_USER') NOT NULL,
        detalles_json       CLOB,
        estado              NUMBER(1)       DEFAULT 1 NOT NULL,
        fecha_creacion      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        CONSTRAINT ck_historial_accion CHECK (accion IN ('crear', 'actualizar', 'eliminar')),
        CONSTRAINT fk_historial_tabla FOREIGN KEY (tabla_id) REFERENCES tablas_sistema (id) ON DELETE CASCADE,
        CONSTRAINT fk_historial_cuenta FOREIGN KEY (cuenta_id) REFERENCES cuentas (id) ON DELETE SET NULL,
        CONSTRAINT ck_historial_cambios_estado CHECK (estado IN (0,1)),
        CONSTRAINT ck_historial_json CHECK (detalles_json IS JSON)
    )
    PARTITION BY RANGE (fecha_creacion)
    INTERVAL (NUMTOYMINTERVAL(1, 'MONTH'))
    (
    PARTITION p_init_historial VALUES LESS THAN (TIMESTAMP '2025-01-01 00:00:00')
    );

    CREATE INDEX idx_historial_fecha   ON historial_cambios (fecha_creacion);
    CREATE INDEX idx_hist_tabla        ON historial_cambios (tabla_id);
    CREATE INDEX idx_hist_cuenta       ON historial_cambios (cuenta_id);

