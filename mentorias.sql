    CREATE INDEX idx_solicitudes_estudiante ON solicitudes_mentoria (estudiante_id);
    CREATE INDEX idx_solicitudes_mentor     ON solicitudes_mentoria (mentor_id);
    CREATE INDEX idx_solicitudes_estado     ON solicitudes_mentoria (estado_solicitud);
    CREATE INDEX idx_solicitudes_fecha      ON solicitudes_mentoria (fecha_hora_deseada);
    CREATE INDEX idx_solicitudes_mentor_estado ON solicitudes_mentoria (mentor_id, estado_solicitud);

    CREATE TABLE sesiones_mentoria (
        id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        solicitud_id        NUMBER          NOT NULL UNIQUE,
        inicio              TIMESTAMP       NOT NULL,
        fin                 TIMESTAMP       NOT NULL,
        enlace_teams        VARCHAR2(4000),
        observaciones       VARCHAR2(4000),
        estado_sesion       VARCHAR2(20)    DEFAULT 'programada' NOT NULL,
        fecha_creacion      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        fecha_actualizacion TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        CONSTRAINT ck_sesiones_rango CHECK (fin > inicio),
        CONSTRAINT fk_sesiones_estado_ses FOREIGN KEY (estado_sesion) REFERENCES cat_estado_sesion (codigo),
        CONSTRAINT fk_sesion_solicitud FOREIGN KEY (solicitud_id) REFERENCES solicitudes_mentoria (id) ON DELETE CASCADE
    )
    PARTITION BY RANGE (fecha_creacion)
    INTERVAL (NUMTOYMINTERVAL(1, 'MONTH'))
    (
    PARTITION p_init_sesiones VALUES LESS THAN (TIMESTAMP '2025-01-01 00:00:00')
    );


    CREATE INDEX idx_sesiones_estado    ON sesiones_mentoria (estado_sesion);
    CREATE INDEX idx_ses_sol_fechas     ON sesiones_mentoria (solicitud_id, inicio, fin, estado_sesion);

    -- CORRECCIÓN J: GENERATED ALWAYS AS VIRTUAL con NUMBER(3,2) y expresión aritmética de enteros.
    -- Oracle requiere que la expresión virtual sea exacta; se usa CAST para asegurar precisión.
    CREATE TABLE calificaciones (
        id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        sesion_id           NUMBER          NOT NULL UNIQUE,
        puntualidad         NUMBER(2)       NOT NULL,
        claridad            NUMBER(2)       NOT NULL,
        dominio_tema        NUMBER(2)       NOT NULL,
        puntaje_total       NUMBER(3,2)     GENERATED ALWAYS AS (ROUND((puntualidad + claridad + dominio_tema) / 3, 2)) VIRTUAL,
        comentario          VARCHAR2(4000),
        estado              NUMBER(1)       DEFAULT 1 NOT NULL,
        fecha_creacion      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        fecha_actualizacion TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        CONSTRAINT ck_calif_puntualidad CHECK (puntualidad BETWEEN 1 AND 5),
        CONSTRAINT ck_calif_claridad CHECK (claridad BETWEEN 1 AND 5),
        CONSTRAINT ck_calif_dominio CHECK (dominio_tema BETWEEN 1 AND 5),
        CONSTRAINT fk_calificacion_sesion FOREIGN KEY (sesion_id) REFERENCES sesiones_mentoria (id) ON DELETE CASCADE,
        CONSTRAINT ck_calificaciones_estado CHECK (estado IN (0,1))
    );



    CREATE TABLE notificaciones (
        id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        cuenta_id           NUMBER          NOT NULL,
        tipo                VARCHAR2(20)    DEFAULT 'sistema' NOT NULL,
        titulo              VARCHAR2(200)   NOT NULL,
        mensaje             VARCHAR2(4000)  NOT NULL,
        leido               NUMBER(1)       DEFAULT 0 NOT NULL,
        solicitud_id        NUMBER,
        sesion_id           NUMBER,
        url_referencia      VARCHAR2(512),
        fecha_leido         TIMESTAMP,
        estado              NUMBER(1)       DEFAULT 1 NOT NULL,
        fecha_creacion      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        fecha_actualizacion TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        CONSTRAINT ck_notificacion_tipo CHECK (tipo IN ('sistema', 'recordatorio', 'logro', 'noticia')),
        CONSTRAINT fk_notificaciones_cuenta FOREIGN KEY (cuenta_id) REFERENCES cuentas (id) ON DELETE CASCADE,
        CONSTRAINT fk_notif_solicitud FOREIGN KEY (solicitud_id) REFERENCES solicitudes_mentoria(id) ON DELETE CASCADE,
        CONSTRAINT fk_notif_sesion FOREIGN KEY (sesion_id) REFERENCES sesiones_mentoria(id) ON DELETE CASCADE,
        CONSTRAINT ck_notificaciones_estado CHECK (estado IN (0,1)),
        CONSTRAINT ck_notificaciones_leido CHECK (leido IN (0,1))
