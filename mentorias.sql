    -- Disponibilidad de horarios del mentor. Horas almacenadas como minutos del día (0-1439)
    -- para evitar problemas de comparación lexicográfica con strings VARCHAR2.
    -- Ejemplo: 14:30 = 870 (14*60+30), 09:00 = 540 (9*60)
    CREATE TABLE cat_dias (
        id NUMBER PRIMARY KEY,
        nombre VARCHAR2(20) NOT NULL UNIQUE
    );

    CREATE TABLE disponibilidad_mentor (
        id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        mentor_id           NUMBER          NOT NULL,
        dia_id              NUMBER          NOT NULL,
        hora_inicio_min     NUMBER(4)       NOT NULL,
        hora_fin_min        NUMBER(4)       NOT NULL,
        activo              NUMBER(1)       DEFAULT 1 NOT NULL,
        estado              NUMBER(1)       DEFAULT 1 NOT NULL,
        fecha_creacion      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        fecha_actualizacion TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
        CONSTRAINT uq_disponibilidad UNIQUE (mentor_id, dia_id, hora_inicio_min),
        CONSTRAINT ck_disp_hora_inicio CHECK (hora_inicio_min BETWEEN 0 AND 1439),
        CONSTRAINT ck_disp_hora_fin CHECK (hora_fin_min BETWEEN 0 AND 1439),
        CONSTRAINT ck_disponibilidad_rango CHECK (hora_fin_min > hora_inicio_min),
        CONSTRAINT ck_disp_duracion_minima CHECK (hora_fin_min - hora_inicio_min >= 30),
        CONSTRAINT fk_disp_dia FOREIGN KEY (dia_id) REFERENCES cat_dias(id),
        CONSTRAINT fk_disponibilidad_mentor FOREIGN KEY (mentor_id) REFERENCES mentores (id) ON DELETE CASCADE,
        CONSTRAINT ck_disponibilidad_mento_estado CHECK (estado IN (0,1))
    );

    CREATE INDEX idx_disponibilidad_mentor ON disponibilidad_mentor (mentor_id);

    CREATE TABLE solicitudes_mentoria (
